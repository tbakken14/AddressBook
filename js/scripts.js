function Contact(firstName = "", lastName = "", streetAddress = "", city = "", state = "",
    state = "", territory = "", country = "", email = "", phone = "") {
    this.firstName = firstName;
    this.lastName = lastName;
    this.streetAddress = streetAddress;
    this.city = city;
    this.state = state;
    this.territory = territory;
    this.country = country;
    this.email = email;
    this.phone = phone;
}
